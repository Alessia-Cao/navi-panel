/**
* @brief  返回x的绝对值
* @param  x:输入数据
* @retval 输出绝对值
*/
s32 GetAbs(s32 x)
{
	s32 tmp;
	tmp = x >= 0 ? x : (-x);
	return tmp;
}

/**
* @brief  返回x的平方根
* @param  x:输入数据
* @retval 平方根
* @note:  输入类型和输出类型为int类型
*/
s32 GetSqurt32(s32 x)
{
	u32 i;
	u32 input_data = (u32)(x);
	u32 rem = 0;     
	u32 root = 0;    
	u32 divisor = 0;    

	for(i = 0; i < 16; i++)
	{      
		root <<= 1;        
		rem = ((rem << 2) + (input_data >> 30));      
		input_data <<= 2;        
		divisor = (root<<1) + 1;      
		if(divisor <= rem)
		{         
			rem -= divisor;        
			root++;       
		}    
	}  
	
	return ((s32)(root));
}


/**
* @brief  返回Sin(x)值
* @param   x 输入角度，范围正负 DEGREE(90)
* @retval 输出正弦值，范围正负4096
*/
s32 GetSin(s32 x)
{
	s32 SinValue;
	s32 index = (DEGREE(90) * 2048 - x * 2048) / (DEGREE(90)*2048/1000); //754975

	if(index > 2000)
		index = 2000;
	else if(index < 0)
		index = 0;
	
	if(GetAbs(x) < 40960)
		SinValue = x * 6434 / 180 / 2048;
	else
		SinValue = SinTable[index];
		
	return SinValue;
}

/**
* @brief  返回Cos(x)值
* @param  x： 输入角度，范围正负 DEGREE(90)
* @retval 输出余弦值，范围正负4096
*/
s32 GetCos(s32 x)
{
	s32 tmp;
	tmp = GetSqurt32(SQUARE(4096) - SQUARE(GetSin(x)));
	return tmp;
}

/**
* @brief  返回Asin(x)值
* @param  x： 输入数据，范围为正负4096
* @retval 输出反正弦值，范围为正负 DEGREE(90)
*/
s32 GetAsin(s32 x)
{
	s32 AsinValue;
	s32 index = (4096 * 4096  - x * 4096) / (4096*4096/1000);
	
	if(index > 2000)
		index = 2000;
	else if(index < 0)
		index = 0;
	
	if(GetAbs(x) < 1060)
		AsinValue = x * 4096 * 180 / 12868; 
	else if(GetAbs(x) < 2633)
	{
		s32 tmp1,tmp2;
		tmp1 = AsinTable[index];
		tmp2 = x * 4096 * 180 / 12868;
		AsinValue = (tmp1 + tmp2) / 2;
	}
	else 
		AsinValue = AsinTable[index];

	return AsinValue;
}

/**
* @brief  返回Atan(x)值
* @param  x： 输入数据
* @param  y： 输入数据，
* @param  flag：选择输出范围
* @retval 输出反正切值，范围为正负737280，或者0 — 1474560
* @note   x,y不能同时为0
*/
s32 GetAtan(s32 x, s32 y, s32 flag)
{
	s32 AtanValue;
	s32 tmp, index, quad_flag;
	s32 abs_x = GetAbs(x);
	s32 abs_y = GetAbs(y);

	if(abs_x >= abs_y)
	{
		quad_flag = 1;
		tmp = abs_y * 4096 / abs_x;
	}
	else 
	{
		quad_flag = 0;
		tmp = abs_x* 4096 / abs_y;
	}
	
	index = tmp * 4096 / 8389;
	
	if(index > 1999)
		index = 1999;
	else if(index < 0)
		index = 0;
	
	if(x == 0)
	{
		if(y == 0)
			AtanValue = 0;		
		else if(y > 0)
			AtanValue = 90*4096;
		else 
			AtanValue = -90*4096;
	}
	else if(y == 0)
	{
		if(x > 0)
			AtanValue = 0;
		else 
			AtanValue = 180*4096;
	}
	else 
	{
		if(quad_flag)
			AtanValue = AtanTable[index];
		else
			AtanValue = DEGREE(90) - AtanTable[index];
		
		if(x > 0 && y > 0);   //第一象限
		else if(x < 0 && y > 0) //第二象限
			AtanValue = 737280 - AtanValue;
		else if(x <0 && y < 0) //第三象限
			AtanValue = AtanValue - 737280;
		else                  //第四象限
			AtanValue = -AtanValue;
	}
	
	//0 — 360度 
	if(flag && (AtanValue < 0))
		AtanValue += 1474560;
	
	return AtanValue;
}

#define LINE_CALC 10

/**
* @brief  由两点表示一条直线
* @param  firstpoint： 第一个点
* @param  secondpoint： 第二个点
* @retval 直线参数
* @note   LineParameter.a代表斜率，LineParameter.b代表长度 LineParameter.flag判断是否为垂直x轴
          当直线为垂直x轴时，LineParameter.a=x轴坐标相交点，LineParameter.flag为ture
*/
LineParameter CalculateLine( const PointCoordinate* firstpoint, const PointCoordinate* secondpoint )
{
    LineParameter param;
    
    if( secondpoint->x != firstpoint->x )
    {
        param.flag = false;
        param.a = (( secondpoint->y - firstpoint->y ) << LINE_CALC)/( secondpoint->x - firstpoint->x );
        param.b = (s64)(secondpoint->y << LINE_CALC) -  (s64)(param.a * secondpoint->x); 
    }
    else
    {
        param.flag = true;
        param.a = secondpoint->x;
        param.b = 0;
    }
    
    return  param;
}

/**
* @brief  通过已知直线和垂点，求垂直线
* @param  line： 已知直线
* @param  foot： 垂点
* @retval 直线参数
* @note   LineParameter.a代表斜率，LineParameter.b代表长度 LineParameter.flag判断是否为垂直x轴
          当直线为垂直x轴时，LineParameter.a=x轴坐标相交点，LineParameter.flag为ture
*/
LineParameter CalculateVerticalLine( const LineParameter* line, const PointCoordinate* foot )
{
    LineParameter vertical;
    
    if( line->flag )
    {
        vertical.a = 0;
        vertical.b = (s64)foot->y << LINE_CALC;
        vertical.flag = false;
    }
    else
    {
        if( line->a == 0 )
        {
            vertical.a = (s64)foot->x;
            vertical.b = 0;
            vertical.flag = true;
        }
        else
        {
            vertical.a = -((s64)(1 << LINE_CALC) * (s64)(1 << LINE_CALC))/line->a;
            vertical.b = ((s64)foot->y << LINE_CALC) - vertical.a * (s64)foot->x;   
            vertical.flag = false;
        }
    }
    
    return  vertical;
}

